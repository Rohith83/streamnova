export function formatDuration(minutes) {
  const h = Math.floor(minutes / 60);
  const m = minutes % 60;
  if (h === 0) return `${m}m`;
  return `${h}h ${m}m`;
}

export function formatRuntimeLabel(rating) {
  if (rating >= 8) return "Critically Acclaimed";
  if (rating >= 7) return "Fan Favorite";
  return "Worth a Watch";
}

export function classNames(...args) {
  return args.filter(Boolean).join(" ");
}

export function debounce(fn, delay = 300) {
  let timer;
  return (...args) => {
    clearTimeout(timer);
    timer = setTimeout(() => fn(...args), delay);
  };
}

export function getInitials(name = "") {
  return name
    .split(" ")
    .map((part) => part[0])
    .join("")
    .slice(0, 2)
    .toUpperCase();
}
